    private void BTNrequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNrequestActionPerformed
        try
        {
            String user_id = TFidno.getText();
            String fname = TFfirstname.getText();
            String mname = TFmiddlename.getText();
            String lname = TFlastname.getText();
            String email = TFemail.getText();
            String contact_no = this.TFcontactnum.getText();
            String yearStarted = this.CByearStarted.getSelectedItem().toString();
            String validation = validateInput(user_id, fname, mname, lname, email);
            String type = getSelectedType();
            if(validation.equals(register_okay)) {
                DB.requestRegistration(user_id, fname, lname, mname, type, email, contact_no, yearStarted);
                Login.start();
                this.stop();
            }
            JOptionPane.showMessageDialog( null, validation );
        }catch(Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }   
    }//GEN-LAST:event_BTNrequestActionPerformed

