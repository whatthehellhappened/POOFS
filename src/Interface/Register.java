    private String validateInput(String user_id, String fname, String mname, String lname, String email) {
        int id = Integer.parseInt(user_id);
        if(user_id.isEmpty() ||fname.isEmpty() || lname.isEmpty() || email.isEmpty() || getSelectedType().equals(""))
            return error_empty;
        else if(DB.hasUser(id)) {
            if(DB.isPending(id))
                return error_pending;
            else
                return error_id;
        } //ID number is not unique
        else if(DB.hasEmail(email))
            return error_email; //Email is already being used
        else
            return register_okay;
    }
    
    private String getSelectedType() {
        for (Enumeration<AbstractButton> buttons = BGtype.getElements(); buttons.hasMoreElements();) {
            AbstractButton button = buttons.nextElement();
            if (button.isSelected()) {
                String type = button.getText();
                if(type.equals("Full-time Faculty"))
                    return "0";
                else if(type.equals("Part-time Faculty"))
                    return "1";
                else
                    return "2";
            }
        }
        return "";
    }
